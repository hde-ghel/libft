#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	char *s2;

	s2 = (char *)s;
	while (*s2 != c)
	{
		if (*s2 == '\0')
			return (NULL);
		s2++;
	}
	return (s2);
}
